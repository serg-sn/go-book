package main

func main() {
	sum := 7 + 5 // sum is 12
	println("sum := 7 + 5, =", sum)
	difference := 7 - 5 // difference is 2
	println("difference := 7 - 5, =", difference)
	product := 7 * 5 // product is 35
	println("product := 7 * 5, =", product)
	quotient := 7 / 5 // quotient is 1
	println("quotient := 7 / 5, =", quotient)
	remainder := 7 % 5 // remainder is 2
	println("remainder := 7 % 5, =", remainder)
}
