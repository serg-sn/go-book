package main

import "fmt"

func main() {
	// Comparison Operators
	fmt.Println(7 == 5) // Output: false
	fmt.Println(7 != 5) // Output: true
	fmt.Println(7 > 5)  // Output: true
	fmt.Println(7 < 5)  // Output: false
	fmt.Println(7 >= 5) // Output: true
	fmt.Println(7 <= 5) // Output: false
	// Logical Operators
	fmt.Println(true && true)  // Output: true
	fmt.Println(true || false) // Output: true
	fmt.Println(!true)         // Output: false

}
